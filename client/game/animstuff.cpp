WORD FindAnimIDFromCRC(DWORD dwCRC32)
{
	WORD wID = 0;

	while (wID != 1812)
	{
		if (dwAnimNameCRC[wID] == dwCRC32)
		{
			return wID;
		}
		wID++;
	}
	return -1;
}

WORD FindAnimIDByName(char* szAnimLib, char* szAnimName)
{
	char szAnim[40];
	WORD wID = 0;

	if (szAnimLib && szAnimName)
	{
		if (strlen(szAnimLib) + strlen(szAnimName) <= 36)
		{
			sprintf_s(szAnim, "%s:%s", szAnimLib, szAnimName);
			Util_strupr(szAnim);

			while (wID != 1812)
			{
				if (strcmp(szAnim, g_szAnimNames[wID]) == 0)
				{
					return wID;
				}
				wID++;
			}
		}
	}
	return -1;
}
