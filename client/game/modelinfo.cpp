void InstallModelInfoPtrsPatch()
{
	DWORD addr,offset;
	BYTE inst;

	int x=0;
	while(x!=707) {
		if(iGtaVersion==GTASA_VERSION_USA10) {
			addr=ModelInfoPatches_USA10[x].addr;
			inst=ModelInfoPatches_USA10[x].inst;
		} else {
			addr=ModelInfoPatches_EU10[x].addr;
			inst=ModelInfoPatches_EU10[x].inst;
		}
		if(inst==*(BYTE*)addr) {
			if(inst==0x8B||inst==0x89||inst==0x39) {
				offset=addr+3;
				//UnFuck(offset,4);
				*(DWORD*)offset=(DWORD)aModelInfoPtrsMemory;
			}
			else if(inst==0xBE||inst==0xBF) {
				offset=addr+1;
				//UnFuck(offset,4);
				*(DWORD*)offset=(DWORD)aModelInfoPtrsMemory;
			}
		}
		x++;
	}
	bModelInfosPatched=true;
}

