bool IsModelPed(int iModelID)
{
	if (iModelID < 0 && iModelID > 30000) return false;

	MODEL_INFO_TYPE* pInfo = GetModelInfo(iModelID);
	if (pInfo) {
		if (pInfo->vtable == 0x85BDC0)
			return true;
	}
	return false;
}