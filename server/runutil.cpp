DWORD CRC32(DWORD crc, char* buf, int len)
{
	unsigned char* t = (unsigned char*)buf;

	crc = ~crc;
	while (len--)
	{
		crc = crc32_table[(crc ^ *t++) & 0xFF] ^ (crc >> 8);
	}
	return ~crc;
}

